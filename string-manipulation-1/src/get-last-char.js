/* 1. Define a function with one parameter, string
2. Use the charAt() method with an argument of string.length - 1 and assign it to a new variable
3. Return the new variable */

function getLastChar(string) {
  var lastChar = string.charAt(string.length - 1);
  return lastChar;
}

